"""
Interactive labelling helper for the `manual` slice of data/eval_set.json.

Why this slice matters
----------------------
Every other slice is generated from the documents it must retrieve, so it leaks
by construction — the only open question is where. Two attempts here made that
concrete:

  * v1 built `topic` queries by rewording titles. Measured afterwards, all 39
    had 100% of their tokens present verbatim in the source title.
  * v2 rebuilt them from abstract terms excluding title tokens. Title overlap
    dropped to 0.00 — and abstract overlap measured 1.00. The leak moved.

A generated query cannot escape this. Hand-written queries can, which makes the
`manual` slice the only one that can settle a dense-vs-BM25 comparison.

What this script does
---------------------
Runs each unlabelled manual query through retrieval with a deliberately wide
net (union of dense, BM25 and hybrid candidates, reranker off so nothing is
pre-filtered by the component under test), then prints numbered candidates for
a human to judge. Judgements are written back into eval_set.json.

Pooled judging, borrowed from TREC: judging a union of systems rather than one
system's output is what keeps the labels from being biased toward whichever
retriever produced them. Labelling only what `hybrid+ce` returned would bake
that configuration's preferences into the ground truth.

Usage:
    # 1. Generate the candidate pool for review
    cd backend && python eval/label_manual.py --pool

    # 2. Edit data/manual_labels.md, marking relevant candidates with [x]

    # 3. Write the judgements back into eval_set.json
    cd backend && python eval/label_manual.py --apply
"""

import argparse
import json
import re
import sys
from pathlib import Path

sys.path.insert(0, ".")
import config

EVAL_SET = Path("data/eval_set.json")
WORKSHEET = Path("data/manual_labels.md")
#: Judgements are ALSO written here, keyed by query text rather than by query
#: id. eval_set.json is regenerated by build_eval_set.py and is gitignored, so
#: labels stored only there are destroyed by the next `make eval-set`. This file
#: is committed: it is hand-produced work that no script can reconstruct, and it
#: is what makes `clone -> make seed -> make ablate` reproduce published numbers.
#: Keying by text (not id) survives regeneration, reordering and re-sampling.
LABELS = Path("data/eval_labels.json")

#: Candidates shown per query. Deep enough that a relevant paper is unlikely to
#: fall outside the pool, shallow enough to stay judgeable in one sitting.
POOL_DEPTH = 12


def _pool_for(query: str) -> list:
    """
    Union of candidates from every retrieval mode, reranker disabled.

    Pooling across configurations is the point: labels drawn from a single
    system's output would encode that system's bias as ground truth.
    """
    from rag import pipeline
    from rag.reranker import Reranker
    from rag.vector_store import VectorStoreManager

    saved_mode = config.RETRIEVAL_MODE
    saved_model = config.RERANKER_MODEL_NAME
    saved_k = config.RETRIEVAL_K
    # Reranker off and a wide top-k: judge what retrieval can reach, not what
    # one ranking of it chose to show.
    config.RERANKER_MODEL_NAME = ""
    config.RETRIEVAL_K = POOL_DEPTH
    Reranker._instance = None

    seen: dict[str, object] = {}
    try:
        for mode in ("dense", "bm25", "hybrid"):
            config.RETRIEVAL_MODE = mode
            try:
                result = pipeline.retrieve_only(query)
            except Exception as exc:
                print(f"    ! {mode} failed: {exc}")
                continue
            for source in result["sources"]:
                pid = source.get("paper_id", "")
                if pid and pid not in seen:
                    seen[pid] = source
    finally:
        config.RETRIEVAL_MODE = saved_mode
        config.RERANKER_MODEL_NAME = saved_model
        config.RETRIEVAL_K = saved_k
        Reranker._instance = None

    docs = {
        d.metadata.get("paper_id", ""): d
        for d in VectorStoreManager.get_or_create().get_all_documents()
    }
    for pid, source in seen.items():
        doc = docs.get(pid)
        source["_abstract"] = (doc.page_content[:300] + "…") if doc else ""
    return list(seen.values())


def build_worksheet() -> None:
    data = json.loads(EVAL_SET.read_text())
    manual = [
        q
        for q in data["queries"]
        if q["query_type"] == "manual" and not q["relevant_paper_ids"]
    ]
    if not manual:
        print("No unlabelled manual queries. Nothing to do.")
        return

    print(f"Pooling candidates for {len(manual)} queries (3 retrieval modes each)…\n")
    lines: list[str] = [
        "# Manual slice labelling worksheet",
        "",
        "Mark every **relevant** paper by changing `- [ ]` to `- [x]`.",
        "",
        "Relevant means: a student who asked this question would consider this",
        "paper genuinely useful — not merely same-field. When unsure, leave it",
        "unchecked; a false positive corrupts the ground truth more than a false",
        "negative, because it rewards retrievers for finding the wrong thing.",
        "",
        "Leave a query with zero checks if nothing in the corpus fits. That is a",
        "real and useful label: it measures the no-results path.",
        "",
        "Then run:  python eval/label_manual.py --apply",
        "",
        "---",
        "",
    ]

    for item in manual:
        print(f"  [{item['id']}] {item['query'][:65]}…")
        candidates = _pool_for(item["query"])
        lines.append(f"## {item['id']}")
        lines.append("")
        lines.append(f"**Query:** {item['query']}")
        lines.append("")
        if not candidates:
            lines.append("_No candidates retrieved — leave unlabelled._")
            lines.append("")
            continue
        for source in candidates:
            title = source.get("title", "(untitled)")
            year = source.get("year", "")
            authors = (source.get("institute_authors") or source.get("authors", ""))[
                :70
            ]
            lines.append(f"- [ ] `{source['paper_id']}` **{title}** ({year})")
            lines.append(f"      _{authors}_")
            lines.append(f"      {source.get('_abstract', '')}")
            lines.append("")
        lines.append("")

    WORKSHEET.parent.mkdir(parents=True, exist_ok=True)
    WORKSHEET.write_text("\n".join(lines))
    print(f"\n  Wrote {WORKSHEET}")
    print("  Mark relevant papers with [x], then: python eval/label_manual.py --apply")


def apply_worksheet(source: str = "human") -> None:
    """
    Write worksheet judgements into eval_set.json.

    `source` records who judged. This is not bookkeeping pedantry: the whole
    value of this slice is that it is *not* produced by the system under test,
    so a model-labelled slice must never be reported as hand-labelled. Model
    labels are useful as a draft and as a relative signal between
    configurations; they cannot settle a question about the model's own
    retrieval, because the labeller shares its priors.
    """
    if not WORKSHEET.exists():
        print(f"{WORKSHEET} not found. Run --pool first.")
        sys.exit(1)

    labels: dict[str, list[str]] = {}
    current: str | None = None
    for line in WORKSHEET.read_text().splitlines():
        heading = re.match(r"^##\s+(\S+)", line)
        if heading:
            current = heading.group(1)
            labels.setdefault(current, [])
            continue
        checked = re.match(r"^\s*-\s*\[[xX]\]\s*`([^`]+)`", line)
        if checked and current:
            labels[current].append(checked.group(1))

    data = json.loads(EVAL_SET.read_text())

    # Persist to the committed label file first, keyed by query text.
    by_id = {q["id"]: q for q in data["queries"]}
    store = (
        json.loads(LABELS.read_text())
        if LABELS.exists()
        else {"label_source": source, "labels": {}}
    )
    store["label_source"] = source
    store.setdefault("labels", {})
    for qid, ids in labels.items():
        query_text = by_id.get(qid, {}).get("query", "")
        if query_text and ids:
            store["labels"][query_text] = ids
    store["note"] = (
        "Hand-judged relevance for the manual slice, keyed by query text. "
        "Applied by build_eval_set.py when the eval set is regenerated."
    )
    LABELS.write_text(json.dumps(store, indent=2, sort_keys=True))

    updated = 0
    for item in data["queries"]:
        if item["id"] in labels and labels[item["id"]]:
            item["relevant_paper_ids"] = labels[item["id"]]
            item["label_source"] = source
            item["note"] = f"{source}-labelled from a pooled candidate set" + (
                " — DRAFT, not human-verified; do not cite as ground truth"
                if source == "model"
                else ""
            )
            updated += 1

    EVAL_SET.write_text(json.dumps(data, indent=2))
    counts = {qid: len(ids) for qid, ids in labels.items()}
    print(f"\n  Applied labels to {updated} queries in {EVAL_SET}")
    print(f"  Persisted to {LABELS} (committed; survives `make eval-set`)")
    for qid, n in sorted(counts.items()):
        flag = "  ← none marked; stays unlabelled" if n == 0 else ""
        print(f"    {qid:<14} {n} relevant{flag}")
    if updated:
        if source == "model":
            print(
                "\n  ⚠ MODEL-LABELLED DRAFT. These labels were produced by the same\n"
                "    model family whose retrieval they will grade, so they cannot\n"
                "    settle a dense-vs-BM25 question — the labeller shares the\n"
                "    system's priors. Useful as a relative signal and as a starting\n"
                "    point for human review; not ground truth. Correct them in\n"
                "    data/manual_labels.md and re-run with --source human."
            )
        else:
            print(
                "\n  The manual slice is now the only slice in the set with no\n"
                "  generation bias. When it disagrees with topic/exact_title, it wins."
            )
        print("\n  Next: python eval/ablate.py --markdown")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Label the manual eval slice")
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument("--pool", action="store_true", help="Build the worksheet")
    group.add_argument("--apply", action="store_true", help="Write labels back")
    parser.add_argument(
        "--source",
        choices=("human", "model"),
        default="human",
        help="Who judged. 'model' marks the labels as an unverified draft.",
    )
    args = parser.parse_args()

    if args.pool:
        build_worksheet()
    else:
        apply_worksheet(source=args.source)
